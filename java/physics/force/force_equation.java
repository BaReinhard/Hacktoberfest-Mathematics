public class ForceEquation {
	/** Calculate the force given an object's mass and acceleration
	 * @param {double} mass - the mass of an object in Kilograms
	 * @param {double} acceleration - the acceleration of an object in m/s^2
	 * @return {double} force - the force in Newtons
	*/
    public static double forceEquation(double mass, double acceleration){

		force = mass * acceleration;
                
		return force;
	}
}
